package com.math;

public class Math_Method {

    public static void main(String[] args){

        System.out.println(Math.max(4, 7));
        System.out.println(Math.min(5,8));
        System.out.println(Math.abs(-89));
        System.out.println(Math.random());
        System.out.println(Math.round(45.456));
        System.out.println(Math.round(78.963));
        System.out.println(Math.sqrt(456));
        System.out.println(Math.pow(12,3));
        System.out.println(Math.ceil(45.04));
        System.out.println(Math.floor(45.99));
    }
}
