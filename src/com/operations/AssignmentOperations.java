package com.operations;

public class AssignmentOperations {

    public static void main (String[] args){
        int number = 100;
        System.out.println("Number present " +number);

        number+=5;
        System.out.println("Number added " +number);

        number-=7;
        System.out.println("Number subracted " +number);

        number*=5;
        System.out.println("number multiplied " +number);

        number/=2;
        System.out.println("number qoutient " +number);

        number%=3;
        System.out.println("number reminder " +number);
    }
}
