package com.operations;

public class RelationalOperations {

    public static void main(String[] args){
        int a = 6;
        int b = 6;

        System.out.println(a == b);
        System.out.println( a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);


        int c = 6;
        int d = 16;

        System.out.println(c == d);
        System.out.println( c != d);
        System.out.println(c > d);
        System.out.println(c < d);
        System.out.println(c >= d);
        System.out.println(c <= d);
    }
}
