package com.datatype;

public class datatype {
    public static void main(String[] args)
    {
        //Char - Initial
        char initial = 'S';
        System.out.println("Initial is " +initial);
        //String - Name
        String name = "Sundari";
        System.out.println("Name is " +name);
        //short - age
        int age = 30;
        System.out.println("Age is " +age);
        //boolean - IT experience
        boolean itExperience = true;
        System.out.println("Experience in IT is " +itExperience);
        //integer - Years of experience
        int yearsOfExperience = 6;
        System.out.println("Years of experience in IT is " +yearsOfExperience+ " years");
        //long - longArray
        long longArray = 60000000L;
        System.out.println("Longest array present here is " +longArray);
        //float pi value
        float piValue = 3.14F;
        System.out.println("Pi value is " +piValue);
        //double temperature
        double temperature = 38.2;
        System.out.println("Temperature is " +temperature+ " degree celsius");

    }
}
